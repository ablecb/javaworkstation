package throwable;

public class Customer {
	private String string;
	private int i;
	public Customer(String string, int i) {
		this.string=string;
		this.i=i;
	}
	public String getString() {
		return string;
	}
	public void setString(String string) {
		this.string = string;
	}
	public int getI() {
		return i;
	}
	public void setI(int i) {
		this.i = i;
	}
	

}
